<p>
Située dans le centre de Valence, La fée picerie existe depuis 1987. Nous sommes fières de pouvoir prétendre à une qualité de service toujours plus performante pour vous. Un large choix de produit est disponible, et depuis peu, nous avons mis en place une boulangerie personnelle. 
</p>

<p>
L’ambiance est chaleureuse, et l’univers nous fait oublier que nous nous situons en ville. Des meubles et étagères en bois, la douce odeur du pain sortie du four inonde notre épicerie. 
</p>

<p>
Nous recensons  plus de 800 produits différents, comprenant des spécialités régionales telles que la crème de marron Sabaton ou leurs marrons glacés. 
Coté cave à vin, nous disposons aussi de vins régionaux comme Le Vendanges d’Octobre, Cornas, Cépages des côtes du Rhône. … 

