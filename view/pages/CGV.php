<h2>Conditions générales de ventes</h2>

	<h3>1. Commande et règlement :</h3>

		<p>Votre commande en ligne a un caractère ferme et définitif. Le paiement est effectué dans les conditions prévues au moment de la commande. 
		Aucune annulation, totale ou partielle de commande définitive ne peut être acceptée, sauf accord écrit de notre part. 
		Aucune marchandise ne peut être renvoyée sans notre consentement préalable et nos instructions de réexpédition (voir article 5 sur les droits de rétractation).</p>

		<p>Lors de la commande sur le site Lafeepicerie.fr, votre commande sera rattachée automatiquement au magasin
		 La fée picerie qui supportera la totale responsabilité de l'exécution de la commande. Toute réclamation, 
		 rétractation devra être formulée sur l'adresse contact@lafeepicerie.fr les prix sont TTC mais n'incluent pas les frais de livraison. 
		 Les frais de livraison sont calculés au panier et sont à la charge du client. Les prix de vente peuvent être modifiés à tout moment ceci avant la prise de commande,
		 les tarifs facturés étant ceux en vigueur lors de l'enregistrement de la commande au panier.</p>

		<p>Toute commande validée sur le site par le client sera effective uniquement après acceptation totale du paiement.</p>

		<p>Le paiement s'effectue en ligne au moyen du système sécurisé par PayPal, Carte bancaire, Visa, MasterCard et via le système de paiement Systempay.
		 La totalité du montant de la commande devra être versé à la commande.</p>

		<p>Afin de sécuriser l'utilisation des moyens de paiement de ses clients, le vendeur peut être amené à demander tous justificatifs 
		(relatifs à l'identité et/ou au domicile et/ou au moyen de paiement utilisé) prouvant votre identité. 
		Le défaut ou le refus de transmettre les justificatifs demandés peut conduire La fée picerie à refuser la commande concernée.</p>

		<p>La fée picerie ne garde pas en mémoire votre numéro de carte de crédit, ni vos coordonnées bancaires. Il vous faudra donc les renseigner à chaque nouvel achat.</p>

		<p>De principe, nos ventes sont payables comptant et sans escompte. En outre, nous nous réservons le droit de facturer 
		les frais bancaires ou d'agios que nous aurions à supporter du fait d'un report d'échéance.</p>
		 
	<h3>2. Délais de livraison :</h3>
