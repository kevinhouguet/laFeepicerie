	<h3>4. Réclamations :</h3>
	
		<p>L'acheteur est tenu de vérifier la marchandise, au moment de la livraison ou de l'enlèvement, et doit mentionner sur le bon de livraison, 
		conformément à l'article L 211-21 du code de la consommation, les réserves qu'il entend faire au sujet de l'état des biens reçus 
		(conformité des produits livrés avec ceux qui ont été commandés, défaut de fabrication, avarie de transport, etc.).  Sauf en cas de vice caché,
		aucune réclamation ne sera admise, après réception des produits par l'acheteur, si celui-ci n'en a pas fait mention sur le bon de livraison.</p>
		
		<p>Toute réclamation doit s'exercer par email et/ou par lettre adressée au magasin émetteur de votre facture (adresse et coordonnées sur votre facture).</p>

	<h3>5. Rétractation :</h3>
	
		<p>Conformément au Code de la consommation, vous disposez d'un droit de rétractation à effectuer dans un délai de 8 (huit) jours francs suivant la livraison de votre commande.</p>
	
		<p>Votre faculté de rétractation doit s'exercer par email et/ou par lettre adressée au magasin émetteur de votre facture (adresse et coordonnées sur votre facture).</p>
		
		<p>Aucune marchandise ne peut être renvoyée sans notre consentement préalable et nos instructions de réexpédition.</p>
		
		<p>Les produits renvoyés en port dû ou en contre remboursement seront systématiquement refusés, 
		les frais liés au retour restant à la charge du client.  Le produit sera obligatoirement repris par le magasin à l'origine de la livraison de votre commande. 
		Si le produit a été livré par transporteur, celui-ci viendra chez le client sur rendez-vous reprendre les produits concernés. Le coût de ce retour sera à la charge du client.</p>
		
		<p>Le remboursement s'effectuera dans un délai maximum de 30 jours après le retour.</p>
		
		<p>Article L 121-20 du code de la consommation  Le consommateur dispose d'un délai de sept jours francs pour exercer son droit de rétractation sans avoir à justifier de motifs 
		ni à payer de pénalités, à l'exception, le cas échéant, des frais de retour. Le consommateur peut déroger à ce délai au cas où il ne pourrait se déplacer et où simultanément
		il aurait besoin de faire appel à une prestation immédiate et nécessaire à ses conditions d'existence. Dans ce cas, il continuerait à exercer son droit de rétractation 
		sans avoir à justifier de motifs ni à payer de pénalités. Le délai mentionné à l'alinéa précédent court à compter de la réception pour les biens ou de l'acceptation de l'offre pour les prestations de services.
		Lorsque les informations prévues à l'article L. 121-19 n'ont pas été fournies, le délai d'exercice du droit de rétractation est porté à trois mois. Toutefois, lorsque la fourniture de ces informations
		intervient dans les trois mois à compter de la réception des biens ou de l'acceptation de l'offre, elle fait courir le délai de sept jours mentionné au premier alinéa. Lorsque le délai de sept jours expire un samedi, un dimanche ou un jour férié ou chômé,
		il est prorogé jusqu'au premier jour ouvrable suivant.</p>

