		<p>Article L 121-20-3 du code de la consommation : Le fournisseur doit indiquer, 
		avant la conclusion du contrat, la date limite à laquelle il s'engage à livrer le bien ou à exécuter la prestation de services. 
		A défaut, le fournisseur est réputé devoir délivrer le bien ou exécuter la prestation de services dès la conclusion du contrat.
		En cas de non-respect de cette date limite, le consommateur peut obtenir la résolution de la vente dans les conditions prévues aux deuxième et troisième alinéas de l'article L 114-1.
		Il est alors remboursé dans les conditions de l'article L 121-20-1  En cas de défaut d'exécution du contrat 
		par un fournisseur résultant de l'indisponibilité du bien ou du service commandé, le consommateur doit être informé de cette indisponibilité et doit,
		le cas échéant, pouvoir être remboursé sans délai et au plus tard dans les trente jours du paiement des sommes qu'il a versées.
		Au-delà de ce terme, ces sommes sont productives d'intérêts au taux légal. Toutefois, si la possibilité en a été prévue préalablement à la conclusion du contrat ou dans le contrat,
		le fournisseur peut fournir un bien ou un service d'une qualité et d'un prix équivalents. Le consommateur est informé de cette possibilité de manière claire et compréhensible.
		Les frais de retour consécutifs à l'exercice du droit de rétractation sont, dans ce cas, à la charge du fournisseur et le consommateur doit en être informé. 
		Le professionnel est responsable de plein droit à l'égard du consommateur de la bonne exécution des obligations résultant du contrat conclu à distance,
		que ces obligations soient à exécuter par le professionnel qui a conclu ce contrat ou par d'autres prestataires de services, sans préjudice de son droit de recours contre ceux-ci. 
		Toutefois, il peut s'exonérer de tout ou partie de sa responsabilité en apportant la preuve que l'inexécution ou la mauvaise exécution du contrat est imputable,
		soit au consommateur, soit au fait, imprévisible et insurmontable, d'un tiers au contrat, soit à un cas de force majeure.</p>

		<p>L'acheteur est pareillement tenu par la date de livraison. Si, après mise à disposition de la marchandise, elle était repoussée par l'acheteur,
		la marchandise sera considérée comme livrée à la date de mise à disposition, entraînant le transfert de la garde, la date de départ de la garantie et
		le cas échéant le financement, en cas de vente à crédit.</p>
		 
